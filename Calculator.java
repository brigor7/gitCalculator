public class Calculator{
	public int addition(int a, int b){
		return a + b;
	}
	
	public double addition(double a, double b){
		return a + b; //return double
	}
	
	public int subtraction(int a, int b){
		return a - b;
	}
	
	public double subtraction(double a, double b){
		return a - b; //return double
	}

}
